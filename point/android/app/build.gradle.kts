fun getApiKey(): String {
    val envFile = File(rootProject.projectDir, "../../.env")
    if (envFile.exists()) {
        val lines = envFile.readLines()
        for (line in lines) {
            if (line.startsWith("GOOGLE_MAPS_API_KEY")) {
                return line.split(":").drop(1).joinToString(":").trim().replace("\"", "").replace("'", "")
            }
        }
    }
    return ""
}

